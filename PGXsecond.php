								<h3>Pharmacogenomics (PGx)</h3>
								<p>Pharmacogenomics looks at how your DNA affects the way you respond to drugs. Pharmacogenomics is an important example of the field of precision medicine, which aims to tailor medical treatment to each person or to a group of people. 
</p>

<h3>Benefits of Pharmacogenomics</h3>

								<p>- Maximize the intended use of a medication or treatment.
</p>
			<p>- Decrease the chance of side effects or dependency. 
.
</p>
			<p>- Speed time to achieving the therapeutic benefit of a drug.
</p>
			<p>- Reduce adverse drug events.
</p>

<h3>Adverse Drug Events Are Responsible For:</h3>

<p>1,000,000 ER visits each year<p>
<p>2,200,000 Hospital Admissions<p>
<p>5,000,000 Office visits<p>
<p>$136 Billion in Healthcare costs<p>

<h3>Pharmacogenomics isn’t new!</h3>

								<p>PGx is the analysis of an individual’s genetic profile and how that affects their drug metabolism. 
Clinical Areas with the largest amount of evidence currently are Psychiatry and Mental Health, Pain Management, Cardiology (this covers medication for anti-arrhythmias like AFIB, Cholesterol lowering meds and blood thinners). Also, antibiotics, hormone therapy, diabetes medication and gut medicines. 
What does the report look like?  It is very thorough, 20+ pages.  Testing for 20 genes and over 250 drugs.</p>


<h3>Per Drug, Individuals Fall under 4 Classes:</h3>

<p>1. Poor metabolizers (PMs) lacking functional enzyme,<p> 
<p>2. Intermediate metabolizers (IMs) being heterozygous for a defect allele
,<p> 
<p>3. Extensive or Efficient metabolizers (EMs) carrying two functional gene copies,<p> 
<p>4. Ultrarapid metabolizers (UMs) carrying more than two functional gene copies.<p> 


<p> This is a once-in-a-lifetime test. Included in the report is a patient card they can be saved and used with any clinician.




